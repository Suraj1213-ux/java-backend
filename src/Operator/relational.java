package Operator;

public class relational {
	
	public static void main (String[] args) {
	// relational operators are use to compare the values 
	// they always return the boolean results 
	
	int a =10;
	int b=20;
	
	System.out.println(a==b);
	System.out.println(a!=b);
	System.out.println(a>b);
	System.out.println(a<b);
	System.out.println(a>=b);
	System.out.println(a<=b);
	
	}
}
