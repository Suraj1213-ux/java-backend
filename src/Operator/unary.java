package Operator;

public class unary {
public static void main(String[] args) {
	int x = 10;
	System.out.println(x++);
	System.out.println(++x);
	System.out.println(x--);
	System.out.println(--x);
	System.out.println(x++ + ++x);
	
	
	
	int a=20;
	System.out.println(--a);
	System.out.println(a++);
	System.out.println(a--);
	System.out.println(a);
	System.out.println(a++ - --a);
	
}
}
